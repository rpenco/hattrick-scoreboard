package org.hattrick.chpp;

/**
 * @author Khips
 * 18 april. 2014
 */
public class CHPPResult {

    private String body = null;
    private int code = 0;

    public CHPPResult() {
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

}
